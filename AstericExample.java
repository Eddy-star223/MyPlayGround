

public class AstericExample {
public static void main(String[] args) {

for (int a = 7; a < 7; a++) {
System.out.print("* ");

for (int b = 1; b > a; b--) {
System.out.print("* ");

}
System.out.println();

}


int wyser = 0;

for (int first = 7; first > wyser; first--) {
System.out.print("* ");

for (int second = 1; second < first; second++) {
System.out.print("* ");

}
System.out.println();

}
System.out.println();


int eddy = 7;

System.out.println();
for (int i = 0; i <= eddy; i++) {
System.out.print("* ");

for (int j = 1; j <= i; j++) {
System.out.print("* ");

}
System.out.println();
}




}
}


